public interface BaseSearchToolbarImpl {
    /**
     * 设置主页面
     */
    void setMainView(int resId);


    /**
     * 设置搜索栏右边按钮
     */
    void setLeftIcon(int resId);

    /**
     * 设置搜索栏右边按钮
     */
    void setSearchClicked(boolean bol);


    /**
     * 点击了搜索回调事件
     */
    void clickSearch(String keyWord);


    /**
     * 历史记录搜索存储
     */
    void historyData(List<String> historyDatas);

    /**
     * 清除历史记录
     */
    void clearHistory();

    /**
     *  设置搜索关键词
     */
    void searchKeyWord(String keyWord);

    /**
     *  点击中间搜索回调时间
     */
    void clickSearchsBox();

    /**
     * 设置标题栏
     */
    void setSearchHint(String hint);
}
