public class DrawbackDetailsBean {

    private boolean success;
    private String   info;
    private DataBean data;
    private String   version;

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public DataBean getData() {
        return data;
    }

    public void setData(DataBean data) {
        this.data = data;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public static class DataBean {

        private String pay_name;
        private double refund_money;
        private String reason_reason;
        private int    refund_status;
        private String remark;
        private String type;

        public String getPay_name() {
            return pay_name;
        }

        public void setPay_name(String pay_name) {
            this.pay_name = pay_name;
        }

        public double getRefund_money() {
            return refund_money;
        }

        public void setRefund_money(double refund_money) {
            this.refund_money = refund_money;
        }

        public int getRefund_status() {
            return refund_status;
        }

        public void setRefund_status(int refund_status) {
            this.refund_status = refund_status;
        }

        public String getReason_reason() {
            return reason_reason;
        }

        public void setReason_reason(String reason_reason) {
            this.reason_reason = reason_reason;
        }

        public String getRemark() {
            return remark;
        }

        public void setRemark(String remark) {
            this.remark = remark;
        }

        public String getType() {
            return type;
        }

        public void setType(String type) {
            this.type = type;
        }
    }
}
