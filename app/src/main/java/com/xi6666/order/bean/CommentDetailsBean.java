public class CommentDetailsBean {

    private boolean success;
    private String   info;
    private DataBean data;
    private String   version;

    public boolean isSuccess() {
        return success;
    }

    public void setSuccess(boolean success) {
        this.success = success;
    }

    public String getInfo() {
        return info;
    }

    public void setInfo(String info) {
        this.info = info;
    }

    public DataBean getData() {
        return data;
    }

    public void setData(DataBean data) {
        this.data = data;
    }

    public String getVersion() {
        return version;
    }

    public void setVersion(String version) {
        this.version = version;
    }

    public static class DataBean {

        private String comment_content;
        private String               comment_level;
        private String               goods_img;
        private List<CommentImgBean> comment_img;

        public String getComment_content() {
            return comment_content;
        }

        public void setComment_content(String comment_content) {
            this.comment_content = comment_content;
        }

        public String getComment_level() {
            return comment_level;
        }

        public void setComment_level(String comment_level) {
            this.comment_level = comment_level;
        }

        public String getGoods_img() {
            return goods_img;
        }

        public void setGoods_img(String goods_img) {
            this.goods_img = goods_img;
        }

        public List<CommentImgBean> getComment_img() {
            return comment_img;
        }

        public void setComment_img(List<CommentImgBean> comment_img) {
            this.comment_img = comment_img;
        }

        public static class CommentImgBean {
            /**
             * photo_source : http://test.xiaoxi6.com/upload/201611/source/xi_1479802969.png
             */

            private String photo_source;

            public String getPhoto_source() {
                return photo_source;
            }

            public void setPhoto_source(String photo_source) {
                this.photo_source = photo_source;
            }
        }
    }
}
